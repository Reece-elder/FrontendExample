// * Http Requests

// * Request Methods

// ! Get
// Requests Data

// Head
// Same as a get, but no response body

// ! Post
// Posts data to the database

// ! Put
// Updates an item within the database 

// ! Delete
// Deletes the specified item

// Connect
// Establishes a tunnel to the server, 

// Options
// Options to describe the commmunication options for the resource

// Trace 
// Performs a message loop-back test along the path to the target resource

// Patch
// Similar to put, partially updates an item in the database 

// * HTTP Status Code

// Codes that come back from HTTP requests to determine the state of the request
// ? 100 - 199 - Informational Response
// ? 200 - 299 - Succesful Responses
// ? 300 - 399 - Redirects
// ? 400 - 499 - Client Errors
// ? 500 - 599 - Server errors